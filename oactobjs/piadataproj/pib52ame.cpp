/// <param name="index">Number of mfb desired.</param>
double Pib52Ame::getAt( int index )
{
#ifndef NDEBUG
  if (index < 0 || index > 205)
    throw PiaException(PIA_IDS_PIB52MFB);
#endif
  return(ame52[index]);
}
